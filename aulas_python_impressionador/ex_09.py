# meta de vendas

meta1 = int(input('Informe o número de vendas do Iphone: '))
meta2 = 30000
if meta1 >= meta2:
    print(f'Meus parabéns sua meta de {meta1} unidades, foi atingida!!')
else:
    print(
        f'Que pena infelizmente a quantidade {meta1} unidades, não atingiu a meta de {meta2} unidades!!')
